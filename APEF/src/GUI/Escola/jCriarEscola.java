    private void addEscolaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEscolaActionPerformed
        // TODO add your handling code here:
        String nome = nEscola.getText();
        String local = nLocal.getText();
        String campo = nCampo.getText();
        
        if(nome.equals("") || local.equals("") || campo.equals(""))
            erroDados.setText("Dados Incompletos");
        else {
            APEF sys = this.root.getSistema();
            Campo c = new Campo(campo);
            Escola e = new Escola(nome, local, c);
            
            boolean res = sys.inserirEscola(e);
            if(res) {
               erroDados.setText("Escola criada com Sucesso");
               root.reloadListaEscolas();
               root.setEnabled(true);
               dispose();
            }
            else
               erroDados.setText("Escola j√° existe");
        }
    }//GEN-LAST:event_addEscolaActionPerformed

